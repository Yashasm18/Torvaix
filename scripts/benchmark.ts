/**
 * Torvaix Benchmark Suite
 * 
 * Measures system performance across critical paths:
 * - Memory retrieval latency
 * - Routing latency (LLM classification)
 * - Tool execution latency
 * - Context build time
 * - Workspace load time
 * 
 * Results are written to BENCHMARKS.md and a timestamped
 * snapshot is saved to benchmarks/history/ for cross-release comparison.
 * 
 * Usage: npx tsx scripts/benchmark.ts
 */

import path from 'path';
import fs from 'fs';
import { MemoryStore } from '@torvaix/memory';

const DB_PATH = path.join(process.cwd(), 'torvaix_benchmark.db');
const BENCHMARKS_MD = path.join(process.cwd(), 'BENCHMARKS.md');
const HISTORY_DIR = path.join(process.cwd(), 'benchmarks', 'history');

// Read version from root package.json
const pkgJson = JSON.parse(fs.readFileSync(path.join(process.cwd(), 'package.json'), 'utf8'));
const VERSION = pkgJson.version || 'unknown';

interface BenchmarkResult {
  name: string;
  iterations: number;
  avgMs: number;
  minMs: number;
  maxMs: number;
  p95Ms: number;
}

async function measure(name: string, fn: () => Promise<void>, iterations: number = 50): Promise<BenchmarkResult> {
  const times: number[] = [];

  // Warmup
  for (let i = 0; i < 3; i++) {
    await fn();
  }

  for (let i = 0; i < iterations; i++) {
    const start = performance.now();
    await fn();
    times.push(performance.now() - start);
  }

  times.sort((a, b) => a - b);
  const avg = times.reduce((s, t) => s + t, 0) / times.length;
  const p95 = times[Math.floor(times.length * 0.95)];

  return {
    name,
    iterations,
    avgMs: Math.round(avg * 100) / 100,
    minMs: Math.round(times[0] * 100) / 100,
    maxMs: Math.round(times[times.length - 1] * 100) / 100,
    p95Ms: Math.round(p95 * 100) / 100,
  };
}

async function runBenchmarks(): Promise<BenchmarkResult[]> {
  console.log('🔬 Torvaix Benchmark Suite');
  console.log(`   Version: ${VERSION}`);
  console.log(`   Date: ${new Date().toISOString()}\n`);

  // Clean benchmark db each run
  if (fs.existsSync(DB_PATH)) fs.unlinkSync(DB_PATH);

  const store = new MemoryStore(DB_PATH);
  const results: BenchmarkResult[] = [];

  // 1. Workspace Load Time
  console.log('⏱  Benchmarking: Workspace Load Time');
  results.push(await measure('Workspace Load', async () => {
    store.listWorkspaces();
  }));

  // 2. Workspace Creation
  console.log('⏱  Benchmarking: Workspace Creation');
  results.push(await measure('Workspace Create', async () => {
    store.createWorkspace('Bench Workspace ' + Math.random());
  }, 30));

  // 3. Memory Store (SQLite-only, no Qdrant)
  console.log('⏱  Benchmarking: Memory Store (SQLite)');
  results.push(await measure('Memory Store', async () => {
    await store.storeMemory('default', `Benchmark memory entry ${Date.now()}`, 'benchmark');
  }, 30));

  // 4. Memory Retrieval (SQLite keyword fallback)
  console.log('⏱  Benchmarking: Memory Retrieval (SQLite Keyword)');
  // Seed some data first
  for (let i = 0; i < 100; i++) {
    await store.storeMemory('default', `Test memory about topic ${i} with keyword alpha beta gamma`, 'seed');
  }
  results.push(await measure('Memory Retrieve', async () => {
    await store.queryMemory('default', 'alpha beta topic', 5);
  }));

  // 5. Pending Action Create (Approval Gate)
  console.log('⏱  Benchmarking: Approval Gate Create');
  results.push(await measure('Approval Gate Create', async () => {
    store.createPendingAction('default', 'bash', { command: 'ls' });
  }, 30));

  // 6. Pending Action Lookup
  console.log('⏱  Benchmarking: Approval Gate Lookup');
  const pendingId = store.createPendingAction('default', 'bash', { command: 'echo test' });
  results.push(await measure('Approval Gate Lookup', async () => {
    store.getPendingAction(pendingId);
  }));

  // 7. Execution Log
  console.log('⏱  Benchmarking: Execution Log Write');
  results.push(await measure('Execution Log Write', async () => {
    store.logExecution('default', 'bash', { command: 'ls' }, 'file1\nfile2', 'success');
  }, 30));

  // 8. Companion Token Create (Experimental)
  console.log('⏱  Benchmarking: Companion Token Create');
  results.push(await measure('Companion Token Create', async () => {
    store.createPairingToken('readonly', 10);
  }, 30));

  // Cleanup
  if (fs.existsSync(DB_PATH)) fs.unlinkSync(DB_PATH);

  return results;
}

function generateMarkdown(results: BenchmarkResult[]): string {
  const now = new Date().toISOString();
  let md = `# Torvaix Benchmarks\n\n`;
  md += `> Auto-generated by \`scripts/benchmark.ts\`\n\n`;
  md += `**Version:** ${VERSION}  \n`;
  md += `**Date:** ${now}  \n`;
  md += `**Environment:** Node ${process.version}, ${process.platform} ${process.arch}\n\n`;

  md += `| Benchmark | Iterations | Avg (ms) | Min (ms) | Max (ms) | P95 (ms) |\n`;
  md += `|-----------|-----------|----------|----------|----------|----------|\n`;
  for (const r of results) {
    md += `| ${r.name} | ${r.iterations} | ${r.avgMs} | ${r.minMs} | ${r.maxMs} | ${r.p95Ms} |\n`;
  }

  md += `\n---\n\n`;
  md += `Historical snapshots are stored in \`benchmarks/history/\` for cross-release comparison.\n`;

  return md;
}

async function main() {
  const results = await runBenchmarks();

  console.log('\n📊 Results:\n');
  for (const r of results) {
    console.log(`  ${r.name.padEnd(25)} avg=${r.avgMs}ms  min=${r.minMs}ms  max=${r.maxMs}ms  p95=${r.p95Ms}ms`);
  }

  // Write BENCHMARKS.md
  const md = generateMarkdown(results);
  fs.writeFileSync(BENCHMARKS_MD, md);
  console.log(`\n✅ Written to ${BENCHMARKS_MD}`);

  // Save historical snapshot
  fs.mkdirSync(HISTORY_DIR, { recursive: true });
  const timestamp = new Date().toISOString().replace(/[:.]/g, '-').replace('T', '_').slice(0, 19);
  const snapshotPath = path.join(HISTORY_DIR, `benchmark_v${VERSION}_${timestamp}.json`);
  fs.writeFileSync(snapshotPath, JSON.stringify({ version: VERSION, date: new Date().toISOString(), results }, null, 2));
  console.log(`📦 Historical snapshot saved to ${snapshotPath}`);
}

main().catch(e => {
  console.error('Benchmark failed:', e);
  process.exit(1);
});
